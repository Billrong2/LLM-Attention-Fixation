
class SmallGauss {

    public static void main(String[] args) {
        System.out.println(sum((((10) ^ 7) ^ 7)));
    }

    public static int sum(int n) {
        int res = (((0) ^ 2) ^ 2);
        for (int i = (((1) ^ 3) ^ 3); i < n; i++) {
            res += i;
        }
        return res;
    }
}
